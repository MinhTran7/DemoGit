package Model;

/**
 *
 * @author DELL
 */
public class Doctor {
    private String id;
    private String name;
    private String major;
    private int course;

    public Doctor(String id, String name, String major, int course) {
        this.id = id;
        this.name = name;
        this.major = major;
        this.course = course;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }

    public int getCourse() {
        return course;
    }

    public void setCourse(int course) {
        this.course = course;
    }
     
}
